export type TranslationKey = keyof typeof translations['en'];

/**
 * Translates a key into the given language.
 *
 * This function is pure and can be used on the server and client.
 *
 * @param lang The current language ('en' or 'fr').
 * @param key The translation key.
 * @param options An optional object for replacing placeholders.
 * @returns The translated string.
 */
export function t(lang: Language, key: TranslationKey, options?: Record<string, string | number>): string {
  const langDict = translations[lang] || translations.en;
  let translation = (langDict as any)[key] || translations.en[key as keyof typeof translations['en']];

  if (!translation) {
      console.warn(`Translation key "${String(key)}" not found for language "${lang}".`);
      return String(key);
  }

  if (options && translation) {
    translation = Object.entries(options).reduce((acc, [optKey, value]) => {
      return acc.replace(`{${optKey}}`, String(value));
    }, translation);
  }
  return translation;
};

    